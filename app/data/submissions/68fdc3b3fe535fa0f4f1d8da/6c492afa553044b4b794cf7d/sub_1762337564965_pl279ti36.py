def TKNP(arr, kt):
	l = 0
	r = len(arr) - 1

	while l <= r:
		g = (l + r) // 2
		if arr[g] == kt:
			return arr[g]
		elif arr[g] < kt:
			l = g + 1
		else:
			r = g - 1
	if arr[g] < kt:
		return arr[g+1]
	return arr[g]
